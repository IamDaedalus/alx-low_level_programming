#include "3-calc.h"

/**
  * op_add - adds two values
  * @a: first value
  * @b: second value
  * Return: returns a and b
  */
int op_add(int a, int b)
{
	return (a + b);
}

/**
  * op_sub - subtracts two values
  * @a: first value
  * @b: second value
  * Return: returns a minus b
  */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
  * op_mul - multiplies two values
  * @a: first value
  * @b: second value
  * Return: returns a times b
  */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
  * op_div - divides two values
  * @a: first value
  * @b: second value
  * Return: returns a divided by b
  */
int op_div(int a, int b)
{
	return (a / b);
}

/**
  * op_mod - mod two values
  * @a: first value
  * @b: second value
  * Return: returns a mod b
  */
int op_mod(int a, int b)
{
	return (a % b);
}

