#include "3-calc.h"

/**
 * op_add - adds two values and returns the result
 * @a: the first number
 * @b: the second number
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_mul - multiplies two values and returns the result
 * @a: the first number
 * @b: the second number
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_sub - subracts two values and returns the result
 * @a: the first number
 * @b: the second number
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_div - divides two values and returns the result
 * @a: the first number
 * @b: the second number
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - divides two values and returns the remainder
 * @a: the first number
 * @b: the second number
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
